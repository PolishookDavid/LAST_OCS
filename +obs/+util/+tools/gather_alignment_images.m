function Info=gather_alignment_images(X,C)
% Example: Info=obs.util.tools.gather_alignment_images(X,C);

Coo = [30  88;...
       30  89;...
       30  90;...
       30  91;...
       30  91.5;...
       30  92;...
       30  92.5;...
       60  88;...
       60  89;...
       60  90;...
       60  91;...
       60  91.5;...
       60  92;...
       60  92.5;...
       60  93;...
       90  90;...
       90  91;...
       90  92;...
       120 88;...
       120 89;...
       120 90;...
       120 91;...
       120 91.5;...
       120 92;...
       120 92.5];
   
Ncoo = size(Coo,1);
for Icoo=1:1:Ncoo
    [Icoo, Ncoo]
    
    X.RA = Coo(Icoo,1);
    pause(10);
    X.Dec = Coo(Icoo,2);
    pause(5);
    
    C.ExpTime = 10;
    C.takeExposure
    
    
    Info(Icoo).Coo = Coo(Icoo,:);
    Info(Icoo).RA  = X.RA;
    Info(Icoo).HA  = X.HA;
    Info(Icoo).Dec = X.Dec;
    Info(Icoo).HATicks = X.HATicks;
    Info(Icoo).DecTicks = X.DecTicks;
    Info(Icoo).JD       = celestial.time.julday - 2./24;
    
    C.waitFinish
    Info(Icoo).ImageName = C.LastImageName;
    save Info.mat Info
    
end

    